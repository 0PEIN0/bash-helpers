alias ${FTP_PROJECT_NAME_UNDERSCORE}_loc="cloc \"$FTP_PROJECT_ROOT_FOLDER\""
alias ${FTP_PROJECT_NAME_UNDERSCORE}_reinitiate="djangoReinitiate \"$FTP_PROJECT_ROOT_FOLDER\" \"$FTP_DJANGO_APP_NAMES\" $FTP_INIT_DATA_LOAD_PYTHON_FILE_PATH \"$FTP_DJANGO_OPERATIONS\""
alias ${FTP_PROJECT_NAME_UNDERSCORE}_reinitiate_clean="djangoReinitiateClean \"$FTP_PROJECT_ROOT_FOLDER\" \"$FTP_DJANGO_OPERATIONS\""
alias ${FTP_PROJECT_NAME_UNDERSCORE}_postgres_shell="psql -U $FTP_POSTGRES_USER"
alias ${FTP_PROJECT_NAME_UNDERSCORE}_postgres_user_password_reset="postgresPasswordReset \"$FTP_INIT_POSTGRES_DB_USER_PASSWORD_FILE_PATH\""
