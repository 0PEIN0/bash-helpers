alias ${FTP_PROJECT_NAME_UNDERSCORE}_ve_installs="djangoDefaultSetup \"$FTP_PROJECT_ROOT_FOLDER\" $FTP_INIT_REQUIREMENTS_FILE_NAME $FTP_PROJECT_NAME_UNDERSCORE"
alias ${FTP_PROJECT_NAME_UNDERSCORE}_installs="${FTP_PROJECT_NAME_UNDERSCORE}_ve_init && cd \"$SYSTEM_ROOT_GIT_REPO_FOLDER\" && git clone ${FTP_GIT_REPO} && ${FTP_PROJECT_NAME_UNDERSCORE}_ve_installs && ${FTP_PROJECT_NAME_UNDERSCORE}_dir"
