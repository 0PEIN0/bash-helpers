FTP_INIT_POSTGRES_DB_USER_PASSWORD_FILE_PATH="$BASH_TEMP_FOLDER/$FTP_POSTGRES_USER_PASSWORD_RESET_FILE_NAME"
FTP_INIT_DATA_LOAD_PYTHON_FILE_PATH="$FTP_PROJECT_ROOT_FOLDER/deployment/$FTP_INIT_DATA_LOAD_PYTHON_FILE_NAME"

if [ -f $BASH_HELPER_GIT_FOLDER/django-project-core.sh ]; then
    . $BASH_HELPER_GIT_FOLDER/django-project-core.sh
fi;

alias ${FTP_PROJECT_NAME_UNDERSCORE}_bc="djangoBranchChange $FTP_PROJECT_NAME_UNDERSCORE "
alias ${FTP_PROJECT_NAME_UNDERSCORE}_bc_full_reset="djangoBranchChangeWithFullReset $FTP_PROJECT_NAME_UNDERSCORE "
alias ${FTP_PROJECT_NAME_UNDERSCORE}_bc_full_reset_and_run="djangoBranchChangeWithFullResetAndRun $FTP_PROJECT_NAME_UNDERSCORE "
alias ${FTP_PROJECT_NAME_UNDERSCORE}_bcd="${FTP_PROJECT_NAME_UNDERSCORE}_bc develop"
alias ${FTP_PROJECT_NAME_UNDERSCORE}_bcd_full_reset="${FTP_PROJECT_NAME_UNDERSCORE}_bc_full_reset develop"
alias ${FTP_PROJECT_NAME_UNDERSCORE}_bcd_full_reset_and_run="${FTP_PROJECT_NAME_UNDERSCORE}_bc_full_reset_and_run develop"
alias ${FTP_PROJECT_NAME_UNDERSCORE}_bcd_run="${FTP_PROJECT_NAME_UNDERSCORE}_bcd && ${FTP_PROJECT_NAME_UNDERSCORE}_run"
alias ${FTP_PROJECT_NAME_UNDERSCORE}_db_shell="${FTP_PROJECT_NAME_UNDERSCORE}_ve && ./manage.py shell"
alias ${FTP_PROJECT_NAME_UNDERSCORE}_dir="goToDir \"$FTP_PROJECT_ROOT_FOLDER\""
