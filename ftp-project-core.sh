alias ${FTP_PROJECT_NAME_UNDERSCORE}_rr="${FTP_PROJECT_NAME_UNDERSCORE}_reset && ${FTP_PROJECT_NAME_UNDERSCORE}_run"
alias ${FTP_PROJECT_NAME_UNDERSCORE}_rt="${FTP_PROJECT_NAME_UNDERSCORE}_reset && ${FTP_PROJECT_NAME_UNDERSCORE}_test"
alias ${FTP_PROJECT_NAME_UNDERSCORE}_run="djangoRun $FTP_PROJECT_NAME_UNDERSCORE $FTP_PROJECT_NAME_UNDERSCORE $FTP_LOCAL_PORT"
alias ${FTP_PROJECT_NAME_UNDERSCORE}_run_production="djangoProductionRun $FTP_PROJECT_NAME_UNDERSCORE $FTP_PROJECT_NAME $FTP_LOCAL_PORT"
