FTP_LOCAL_PORT="8066"
FTP_DJANGO_APP_NAMES="commons permissions rest users"
FTP_POSTGRES_USER="postgres"
FTP_CELERY_FLOWER_HOST="127.0.0.1"
FTP_CELERY_FLOWER_PORT="5566"

FTP_DJANGO_OPERATIONS="makemigrations migrate"
FTP_INIT_REQUIREMENTS_FILE_NAME='requirements.txt'
FTP_PROJECT_ROOT_FOLDER="$SYSTEM_ROOT_GIT_REPO_FOLDER/$FTP_REPO_NAME"
FTP_VIRTUAL_ENVIRONMENT_NAME="${FTP_PROJECT_NAME_CAMEL_CASE}Env"
